# and, or, not
a, b = 4, 7
print(a > 6 and b > 6)   # output: False
print(a > 6 or b > 6)    # output: True

a, b = 3, 6
print(a > 2 and b > 2)   # output: True
print(a > 2 or b > 2)    # output: True

a = 4
print(not(a))       # output: False

a = True
print(not(a))       # output: False